#include <stdio.h>
#include <stdlib.h>

int main()
{
	struct student {
		int number;
		char name[10];
		double grade;
	};

	struct student s;
	
	s.number = 20070001;
	strcpy(s.name, "홍길동");
	s.grade = 4.3;

	printf("학번: %d\n", s.number);
	printf("이름: %s\n", s.name);
	printf("학점: %f\n", s.grade);

	return 0;
}